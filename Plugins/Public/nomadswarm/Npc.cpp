void NPC::CreateSwarmNPC(uint swarmTargetObj, Vector spawnPos, Matrix spawnRot, uint spawnSystem)
{
	// Randomly place the NPC 12k away from the central ship - Create 3 X/Y/Z coordinates with a radius of 15k away from the swarmtarget
	float x = createRandomGuassianNumber();
	float y = createRandomGuassianNumber();
	float z = createRandomGuassianNumber();

	x = x * (1 / sqrt((x * x) + (y * y) + (z * z)));
	y = y * (1 / sqrt((x * x) + (y * y) + (z * z)));
	z = z * (1 / sqrt((x * x) + (y * y) + (z * z)));

	// Each should have a radius of 12k
	x = x * 12000;
	y = y * 12000;
	z = z * 12000;
	
	pub::SpaceObj::ShipInfo si;
	memset(&si, 0, sizeof(si));
	si.iFlag = 1;
	si.iSystem = spawnSystem;
	si.iShipArchetype = CreateID("dsy_civbomb");
	si.vPos = spawnPos;
	si.vPos.x = spawnPos.x + x;
	si.vPos.y = spawnPos.y + y;
	si.vPos.z = spawnPos.z + z;
	si.mOrientation = spawnRot;
	si.iLoadout = CreateID("event_boss_cultist_bmb_scorcher_loadout");
	si.iLook1 = CreateID("li_newscaster_head_gen_hat");
	si.iLook2 = CreateID("pl_female1_journeyman_body");
	si.iComm = CreateID("comm_br_darcy_female");
	si.iPilotVoice = CreateID("pilot_f_leg_f01a");
	si.iHealth = -1;
	si.iLevel = 19;

	uint iSpaceObj;
	pub::SpaceObj::Create(iSpaceObj, si);

	pub::AI::SetPersonalityParams pers = HkMakePersonality();
	pub::AI::SubmitState(iSpaceObj, &pers);

	SwarmNpc npc;
	npc.spaceObj = iSpaceObj;
	npc.swarmTargetObj = swarmTargetObj;

	spawnedNpcIds.push_back(npc);
}
